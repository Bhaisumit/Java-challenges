public class swap {
    public static void main(String[] args) {
        int a=2;
        int b=3;
        System.out.println("lets create a program to swap two numbers:");
        System.out.println("value of a is " + a);
        System.out.println("value of b is " + b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println("now after swaping");
        System.out.println("value of a is " + a);
        System.out.println("value of b is " + b);

    }
}
