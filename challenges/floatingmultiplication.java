import java.util.*;
public class floatingmultiplication {
    public static void main(String[] args) {
        Scanner input=new Scanner(System.in);
        System.out.println("let do multiplication of floating point numbers");
        System.out.println("write first input");
        double pehla=input.nextDouble();
        System.out.println("Write second input");
        double dusra=input.nextDouble();
        System.out.println("multiplication of these floating point numbers is " + pehla*dusra);

    }
}
